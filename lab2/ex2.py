import math


def hypotenuse(a, b):
    """
    Функція для обчислення довжини гіпотенузи прямокутного трикутника
    Параметри:
        a (float): довжина катета a
        b (float): довжина катета b
    Повертає:
        float: довжина гіпотенузи
    """
    return math.sqrt(a**2 + b**2)

# Введення катетів для двох трикутників
print("Введіть катети для першого трикутника:")
a1 = float(input("Катет a: "))
b1 = float(input("Катет b: "))

print("\nВведіть катети для другого трикутника:")
a2 = float(input("Катет a: "))
b2 = float(input("Катет b: "))

# Обчислення гіпотенуз
hyp1 = hypotenuse(a1, b1)
hyp2 = hypotenuse(a2, b2)

# Порівняння гіпотенуз
if hyp1 > hyp2:
    print(f"\nГіпотенуза першого трикутника ({hyp1}) більша за гіпотенузу другого трикутника ({hyp2}).")
elif hyp1 < hyp2:
    print(f"\nГіпотенуза першого трикутника ({hyp1}) менша за гіпотенузу другого трикутника ({hyp2}).")
else:
    print(f"\nГіпотенузи обох трикутників однакові ({hyp1}).")